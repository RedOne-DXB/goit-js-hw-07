const increment = function (i) {
    for (let i = 0; i < increment.length; i++) {
        let count = 0;
        count += i;

        return count;
    }
}

let i = 5;
console.log(increment(i))

const decrement = function (y) {
    for (let y = 0; y < decrement.length; y--) {
        let count = 0;
        count -= y;

        return count;
    }
}

console.log(decrement(10))

